import 'package:cloning_payuung_pribadi/src/features/profile/data/collections/profile_collection.dart';
import 'package:cloning_payuung_pribadi/src/features/profile/domain/entities/profile.dart';

class ProfileModel extends Profile {
  const ProfileModel({
    super.id,
    super.fullName,
    super.email,
    super.dateOfBirth,
    super.phoneNumber,
    super.gender,
    super.education,
    super.statusMarried,
    super.identityCardNumber,
    super.province,
    super.addressIdentityCard,
    super.fullAddress,
    super.district,
    super.vilage,
    super.poscode,
    super.companyName,
    super.addressCompany,
    super.potitionInCompany,
    super.durationWork,
    super.bankName,
    super.bankNumber,
    super.nameOwnerBank,
    super.branchBank,
    super.sourceIncome,
    super.identityCardFileName,
    super.grossIncomePerYear,
    super.subDistrict,
    super.photoProfile,
  });

  factory ProfileModel.fromJson(Map<String, dynamic> json) {
    return ProfileModel(
      id: json["id"],
      fullName: json['fullName'],
      email: json['email'],
      dateOfBirth: json['dateOfBirth'],
      phoneNumber: json['phoneNumber'],
      gender: json['gender'],
      education: json['education'],
      statusMarried: json['statusMarried'],
      identityCardNumber: json['identityCardNumber'],
      province: json['province'],
      addressIdentityCard: json['addressIdentityCard'],
      fullAddress: json['fullAddress'],
      district: json['district'],
      vilage: json['vilage'],
      poscode: json['poscode'],
      companyName: json['companyName'],
      addressCompany: json['addressCompany'],
      potitionInCompany: json['potitionInCompany'],
      durationWork: json['durationWork'],
      bankName: json['bankName'],
      bankNumber: json['bankNumber'],
      nameOwnerBank: json['nameOwnerBank'],
      branchBank: json['branchBank'],
      sourceIncome: json['sourceIncome'],
      grossIncomePerYear: json['grossIncomePerYear'],
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'fullName': fullName,
      'email': email,
      'dateOfBirth': dateOfBirth,
      'phoneNumber': phoneNumber,
      'gender': gender,
      'education': education,
      'statusMarried': statusMarried,
      'identityCardNumber': identityCardNumber,
      'province': province,
      'addressIdentityCard': addressIdentityCard,
      'fullAddress': fullAddress,
      'district': district,
      'vilage': vilage,
      'poscode': poscode,
      'companyName': companyName,
      'addressCompany': addressCompany,
      'potitionInCompany': potitionInCompany,
      'durationWork': durationWork,
      'bankName': bankName,
      'bankNumber': bankNumber,
      'nameOwnerBank': nameOwnerBank,
      'branchBank': branchBank,
      'sourceIncome': sourceIncome,
      'grossIncomePerYear': grossIncomePerYear,
    };
  }

  toCollection() => ProfileCollection(
        id!.toInt(),
        fullName,
        email,
        dateOfBirth,
        phoneNumber,
        gender,
        education,
        statusMarried,
        identityCardNumber,
        province,
        addressIdentityCard,
        fullAddress,
        district,
        vilage,
        poscode,
        companyName,
        addressCompany,
        potitionInCompany,
        durationWork,
        bankName,
        bankNumber,
        nameOwnerBank,
        branchBank,
        sourceIncome,
        grossIncomePerYear,
        identityCardFileName,
        subDistrict,
        photoProfile,
      );
}
